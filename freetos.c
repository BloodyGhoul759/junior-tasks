/**
 * @brief Function implementing the Task2 thread.
 * @param argument: Not used
 * @retval None
 */
/* USER CODE END Header_Task2_init */
void Task2_init(void *argument)
{
  /* USER CODE BEGIN Task2_init */

	int bt1Debounce = 0;

	char *on = "led is on";
	char *off = "led is off";

	/* Infinite loop */
	for (;;) {
		if ((GPIOA->IDR & (1 << 0)) && bt1Debounce == 0) {
			bt1Debounce = 1;
		}
		if (!(GPIOA->IDR & (1 << 0)) && bt1Debounce == 1) {// if button is pressed

			GPIOA->ODR ^= GPIO_PIN_3; // toggle the led

			if ((GPIOA->IDR & (1 << 3))) {// send uart message depending on led status
			HAL_UART_Transmit(&huart1, (uint8_t*) on, strlen(on), 100);
			} else
				HAL_UART_Transmit(&huart1, (uint8_t*) off, strlen(off), 100);
			bt1Debounce = 0;
		}
		osDelay(20);
	}
  /* USER CODE END Task2_init */
}

