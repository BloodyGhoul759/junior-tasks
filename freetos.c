/**
 * @brief Function implementing the Task3 thread.
 * @param argument: Not used
 * @retval None
 */
/* USER CODE END Header_Task3_init */
void Task3_init(void *argument)
{
  /* USER CODE BEGIN Task3_init */

	int bt2Debounce = 0;
	int timer = TIM2->CNT;
	char str[50]; //temp string
	int IsLedON = 0; // just read the name DUH
	int freq[] = { 2000, 1000, 500, 250, 100 };// the freq we need
	int i = 0;

	/* Infinite loop */
	for (;;) {

		if ((GPIOA->IDR & (1 << 2)) && bt2Debounce == 0) {
			bt2Debounce = 1;
		}
		if (!(GPIOA->IDR & (1 << 2)) && bt2Debounce == 1) {// if button is pressed
			if (IsLedON == 1) {// if led is on
				if (i >= 5)// if index is out of range fix it
					i = 0;
				else
					i++;
				sprintf(str, "current Freq = %d", freq[i]);
				HAL_UART_Transmit(&huart1, (uint8_t*) str, strlen(str), 100);
			} else
				IsLedON = 1;
			bt2Debounce = 0;

		}
		if (TIM2->CNT - timer >= freq[i] && IsLedON == 1) {
			GPIOA->ODR ^= GPIO_PIN_5;
			timer = TIM2->CNT;
		}

		osDelay(20);
	}
  /* USER CODE END Task3_init */
}

